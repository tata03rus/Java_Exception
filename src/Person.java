public class Person {
    String name;
    String lastname;
    String fatherName;
    String dateOfBirtday;
    Long number;
    String gender;

    public String getName() {
        return name;
    }

    public String getLastname() {
        return lastname;
    }

    public String getFatherName() {
        return fatherName;
    }

    public String getDateOfBirtday() {
        return dateOfBirtday;
    }

    public Long getNumber() {
        return number;
    }

    public String getGender() {
        return gender;
    }
}